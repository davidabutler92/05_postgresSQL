DROP TABLE IF EXISTS animals;

CREATE TABLE animals (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    color TEXT NOT NULL,
    type TEXT NOT NULL
);

CREATE TABLE foods (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    color TEXT NOT NULL,
    type TEXT NOT NULL
);

CREATE TABLE cars (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    color TEXT NOT NULL,
    type TEXT NOT NULL
);

CREATE TABLE sports (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    color TEXT NOT NULL,
    type TEXT NOT NULL
);

CREATE TABLE pokemon (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    color TEXT NOT NULL,
    type TEXT NOT NULL
);
